// == : igualdad
// === : para comparar igualdad en valor y en tipo de dato
// != : es diferente a, true: si es diferente o false: si es igual
// !==  : es diferente en valor y en tipo de datos 

// <  : mayor que 
// > : menor que 
// >= : mayor o igual que 
// <=  : menor o igual que 

const a = 10;
const b = 20;
const c = "10"
console.log( a == b) //false
console.log( a === c) //false
console.log( a == c) //true
console.log (a != c) //false -> si se interpreta como iguales
console.log (a !== c) //true -> porque es diferente en tipo de dato
